def get_group_titles(conn, gids):
    c = conn.cursor()
    result = {}
    for gid in gids:
        c.execute("SELECT DISTINCT title FROM story_detail WHERE story_group_id = ?", (gid,))
        titles = [r[0] for r in c.fetchall() if r[0]]
        result[gid] = titles
    return result

